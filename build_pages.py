def make_chart_id(name, idx):
    return f"chart_{name.replace(' ', '_')}_{idx}"


def build_numeric_chart(fname, fstats, chart_id):
    edges = fstats["histogram"]["edges"]
    counts = fstats["histogram"]["counts"]
    labels = [f"{edges[i]:.2f}" for i in range(len(counts))]
    stats_html = (f'<span>mean: <b>{fstats["mean"]}</b></span>'
                  f'<span>std: <b>{fstats["std"]}</b></span>'
                  f'<span>min: <b>{fstats["min"]}</b></span>'
                  f'<span>max: <b>{fstats["max"]}</b></span>')
    card_html = (f'<div class="chart-card">'
                 f'<h3>{fname}</h3>'
                 f'<div class="stats">{stats_html}</div>'
                 f'<canvas id="{chart_id}"></canvas>'
                 f'</div>')
    script = (f'new Chart(document.getElementById("{chart_id}"), {{'
              f'type:"bar",'
              f'data:{{labels:{json.dumps(labels)},'
              f'datasets:[{{label:"{fname}",data:{json.dumps(counts)},'
              f'backgroundColor:"rgba(88,166,255,0.6)",borderColor:"#58a6ff",borderWidth:1}}]}},'
              f'options:{{responsive:true,plugins:{{legend:{{display:false}}}},'
              f'scales:{{x:{{ticks:{{color:"#8b949e",maxTicksLimit:8}},'
              f'grid:{{color:"#21262d"}}}},'
              f'y:{{ticks:{{color:"#8b949e"}},grid:{{color:"#21262d"}}}}}}}}}});')
    return card_html, script


def build_categorical_chart(fname, fstats, chart_id):
    vc = fstats["value_counts"]
    labels = list(vc.keys())
    values = list(vc.values())
    colors = ["#58a6ff","#3fb950","#f78166","#d2a8ff","#ffa657","#79c0ff","#56d364"]
    bg = [colors[i % len(colors)] for i in range(len(labels))]
    card_html = (f'<div class="chart-card">'
                 f'<h3>{fname}</h3>'
                 f'<div class="stats"><span>{len(labels)} categories</span></div>'
                 f'<canvas id="{chart_id}"></canvas>'
                 f'</div>')
    script = (f'new Chart(document.getElementById("{chart_id}"), {{'
              f'type:"doughnut",'
              f'data:{{labels:{json.dumps(labels)},'
              f'datasets:[{{data:{json.dumps(values)},'
              f'backgroundColor:{json.dumps(bg)},'
              f'borderColor:"#161b22",borderWidth:2}}]}},'
              f'options:{{responsive:true,'
              f'plugins:{{legend:{{position:"bottom",labels:{{color:"#8b949e",'
              f'padding:8,font:{{size:11}}}}}}}}}}}});')
    return card_html, script


def build_binary_chart(fname, fstats, chart_id):
    pos = fstats["positive_count"]
    neg = fstats["negative_count"]
    rate = fstats["positive_rate"]
    card_html = (f'<div class="chart-card">'
                 f'<h3>{fname}</h3>'
                 f'<div class="stats"><span>positive rate: <b>{rate:.1%}</b></span></div>'
                 f'<canvas id="{chart_id}"></canvas>'
                 f'</div>')
    script = (f'new Chart(document.getElementById("{chart_id}"), {{'
              f'type:"bar",'
              f'data:{{labels:["Negative (0)","Positive (1)"],'
              f'datasets:[{{label:"{fname}",data:[{neg},{pos}],'
              f'backgroundColor:["rgba(56,211,100,0.6)","rgba(247,129,102,0.6)"],'
              f'borderColor:["#3fb950","#f78166"],borderWidth:1}}]}},'
              f'options:{{responsive:true,plugins:{{legend:{{display:false}}}},'
              f'scales:{{x:{{ticks:{{color:"#8b949e"}},grid:{{color:"#21262d"}}}},'
              f'y:{{ticks:{{color:"#8b949e"}},grid:{{color:"#21262d"}}}}}}}}}});')
    return card_html, script


def build_dataset_page(meta, stats, output_dir, repo="PranayMahendrakar/ai-dataset-generator"):
    charts_html_parts = []
    script_parts = []
    for i, (fname, fstats) in enumerate(stats.items()):
        cid = make_chart_id(fname, i)
        ftype = fstats.get("type", "unknown")
        if ftype == "numeric":
            ch, sc = build_numeric_chart(fname, fstats, cid)
        elif ftype == "categorical":
            ch, sc = build_categorical_chart(fname, fstats, cid)
        elif ftype == "binary":
            ch, sc = build_binary_chart(fname, fstats, cid)
        else:
            continue
        charts_html_parts.append(ch)
        script_parts.append(sc)

    ds_name = meta["dataset"]
    html = HTML_TEMPLATE.format(
        dataset_title=ds_name.replace("_", " ").title(),
        dataset_description=meta.get("description", ""),
        dataset_name=ds_name,
        size=meta["size"],
        num_features=meta["features"],
        noise=meta["noise"],
        target_column=meta.get("target_column", "N/A"),
        generated_at=meta.get("generated_at", "")[:10],
        charts_html="\n".join(charts_html_parts),
        chart_scripts="\n".join(script_parts),
        repo=repo
    )
    out_path = os.path.join(output_dir, f"{ds_name}.html")
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"[PAGES] Built -> {out_path}")
    return ds_name, meta


def build_index(datasets_info, output_dir, repo="PranayMahendrakar/ai-dataset-generator"):
    cards = []
    for ds_name, meta in datasets_info:
        title = ds_name.replace("_", " ").title()
        desc = meta.get("description", "")
        tags = [f"{meta['size']:,} rows", f"{meta['features']} features", f"noise={meta['noise']}",
                meta.get("target_column", "")]
        tag_html = "".join(f'<span class="tag">{t}</span>' for t in tags if t)
        cards.append(f'<div class="card"><h2>{title}</h2><p>{desc}</p>'
                     f'<div class="tags">{tag_html}</div>'
                     f'<a href="{ds_name}.html">View Visualizations →</a></div>')

    html = INDEX_TEMPLATE.format(
        dataset_cards="\n".join(cards),
        repo=repo
    )
    idx_path = os.path.join(output_dir, "index.html")
    with open(idx_path, "w", encoding="utf-8") as f:
        f.write(html)
    print(f"[PAGES] Built index -> {idx_path}")


def main():
    output_dir = os.environ.get("OUTPUT_DIR", "output")
    docs_dir = os.environ.get("DOCS_DIR", "docs")
    os.makedirs(docs_dir, exist_ok=True)

    meta_files = glob.glob(os.path.join(output_dir, "*_metadata.json"))
    datasets_info = []

    for mf in meta_files:
        ds_name = os.path.basename(mf).replace("_metadata.json", "")
        stats_file = os.path.join(output_dir, f"{ds_name}_stats.json")
        if not os.path.exists(stats_file):
            print(f"[WARN] No stats for {ds_name}, skipping")
            continue
        with open(mf) as f:
            meta = json.load(f)
        with open(stats_file) as f:
            stats = json.load(f)
        info = build_dataset_page(meta, stats, docs_dir)
        datasets_info.append(info)

    if datasets_info:
        build_index(datasets_info, docs_dir)
        print(f"[PAGES] Done! {len(datasets_info)} dataset(s) ready in {docs_dir}/")
    else:
        print("[WARN] No datasets found. Run generate_dataset.py first.")


if __name__ == "__main__":
    main()
